#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*str;

	str = malloc(ft_strlen(s) + 2);
	if (!str)
		return (NULL);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = ' ';
	str[i + 1] = '\0';
	return (str);
}
